export function addTwoNumbers(num1:number, num2:number):number{
    return num1+num2;
}
export function addTwoNumbers2(num1:number, num2:number):number{
    return num1-num2;
}
export function addTwoNumbers3(num1:number, num2:number):number{
    return num1*num2;
}
export function addTwoNumbers4(num1:number, num2:number):number{
    return num1/num2;
}
